/**
  * @brief This function handles UART5 global interrupt.
  */
void UART5_IRQHandler(void)
{
  /* USER CODE BEGIN UART5_IRQn 0 */

  /* USER CODE END UART5_IRQn 0 */
  HAL_UART_IRQHandler(&huart5);
  /* USER CODE BEGIN UART5_IRQn 1 */

    /* 自动恢复：HAL_UART_IRQHandler 检测到 ORE 时会调用
       UART_EndRxTransfer + HAL_DMA_Abort 杀掉 DMA（同 USART10 问题）。
       此处检测并重启 SBUS 双缓冲 DMA 接收。 */
    {
        DMA_Stream_TypeDef *dma = (DMA_Stream_TypeDef *)huart5.hdmarx->Instance;
        if ((dma->CR & DMA_SxCR_EN) == 0U)
        {
            __HAL_UART_CLEAR_OREFLAG(&huart5);
            (void)huart5.Instance->RDR;
            huart5.ErrorCode = HAL_UART_ERROR_NONE;
            huart5.RxState = HAL_UART_STATE_READY;

            USART_RxDMA_MultiBufferStart(&huart5,
                (uint32_t *)&(huart5.Instance->RDR),
                (uint32_t *)SBUS_MultiRx_Buf[0],
                (uint32_t *)SBUS_MultiRx_Buf[1], 36);
        }
    }

  /* USER CODE END UART5_IRQn 1 */
}

