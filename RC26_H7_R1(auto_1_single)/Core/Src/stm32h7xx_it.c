/**
  * @brief This function handles USART10 global interrupt.
  */
void USART10_IRQHandler(void)
{
  /* USER CODE BEGIN USART10_IRQn 0 */
    if (__HAL_UART_GET_FLAG(&huart10, UART_FLAG_IDLE) &&
        __HAL_UART_GET_IT_SOURCE(&huart10, UART_IT_IDLE))
    {
        __HAL_UART_CLEAR_IDLEFLAG(&huart10);
        REMOTE_IdleHandler();
    }
  /* USER CODE END USART10_IRQn 0 */
  HAL_UART_IRQHandler(&huart10);
  /* USER CODE BEGIN USART10_IRQn 1 */

    /* 自动恢复：HAL_UART_IRQHandler 在检测到 ORE 等错误时
       (stm32h7xx_hal_uart.c:2294-2317) 会清掉 CR3.DMAR 并
       HAL_DMA_Abort 杀掉 DMA 流。这里检测并重新启动接收。 */
    {
        DMA_Stream_TypeDef *dma = (DMA_Stream_TypeDef *)huart10.hdmarx->Instance;
        if ((dma->CR & DMA_SxCR_EN) == 0U)
        {
            /* 清除残留错误标志 */
            __HAL_UART_CLEAR_OREFLAG(&huart10);
            (void)huart10.Instance->RDR;
            huart10.ErrorCode = HAL_UART_ERROR_NONE;

            /* 强制重置状态并重启 DMA */
            huart10.RxState = HAL_UART_STATE_READY;
            HAL_UART_Receive_DMA(&huart10, REMOTE_BUF, REMOTE_BUF_SIZE);

            /* 兜底：确保 DMA 确实使能了 */
            if ((dma->CR & DMA_SxCR_EN) == 0U)
            {
                dma->NDTR = REMOTE_BUF_SIZE;
                dma->CR |= DMA_SxCR_EN;
            }
            huart10.Instance->CR3 |= USART_CR3_DMAR;

            /* 确保 IDLE 中断使能（HAL 可能关了） */
            __HAL_UART_ENABLE_IT(&huart10, UART_IT_IDLE);

            /* 复位帧解析状态机，因为缓冲区可能有脏数据 */
            remote_last_pos = 0;
        }
    }

  /* USER CODE END USART10_IRQn 1 */
}

